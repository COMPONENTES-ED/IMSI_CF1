        <h2 id="t_3_2">3.2 Formato y registros de servicio</h2>
      </div>
      <p data-aos="fade-right">
        El uso de formatos y registros de servicio en el soporte técnico y
        atención al cliente para la reparación de equipos es empleada para
        asegurar una gestión organizada, transparente y eficiente. Estos
        documentos permiten capturar y documentar cada paso del proceso, desde
        el reporte inicial del cliente hasta la resolución final y el cierre del
        caso. Además de ser una herramienta de registro, estos formatos
        facilitan el análisis y la mejora continua del servicio, optimizando el
        uso de las herramientas de gestión en el soporte técnico.
      </p>
      <p data-aos="fade-left">
        Importancia del uso de formatos y registros de servicio:
      </p>
