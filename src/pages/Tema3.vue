          <div class="tarjeta-numerada color-secundario p-5 h-100">
            <div class="tarjeta-numerada__numero">
              <div class="h2">2</div>
            </div>
            <h5>Optimización de los procesos de escalamiento.</h5>
            <p class="mb-0">
              Cuando un problema no se resuelve en el primer nivel, los
              registros de servicio permiten a los técnicos de niveles
              superiores entender de inmediato lo que se ha intentado y las
              conclusiones alcanzadas. Esto evita repeticiones de esfuerzos y
              reduce los tiempos de resolución al facilitar un escalamiento
              organizado y eficiente.
            </p>
          </div>
        </div>
        <div
          class="col-md-6 col-xl mb-4 mb-xl-0"
          data-aos="zoom-in"
          data-aos-duration="850"
        >
          <div class="tarjeta-numerada color-acento-contenido p-5 h-100">
            <div class="tarjeta-numerada__numero">
              <div class="h2">3</div>
            </div>
            <h5>Cumplimiento de <em>SLAs</em> y evaluación del rendimiento.</h5>
            <p class="mb-0">
              Los registros ayudan a cumplir con los <em>SLAs</em> (Acuerdos de
              Nivel de Servicio) al documentar los tiempos de respuesta y de
              resolución de cada caso. A través de estos registros, el equipo de
              soporte puede evaluar su rendimiento y detectar áreas que
              necesitan mejoras, como los tiempos de atención en incidentes de
              alta prioridad.
            </p>
